#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*cpy;
	size_t	size;
	size_t	i;

	i = 0;
	size = ft_strlen(s1) + 1;
	cpy = (char *)ft_malloc(size);
	while (s1[i] != '\0')
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
